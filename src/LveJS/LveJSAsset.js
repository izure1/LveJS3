class LveJSAsset {

  name = null
  url = null
  blob = null

  setName(name) {
    this.name = name
  }
  setURL(url) {
    this.url = url
  }
  setBlob(blob) {
    this.blob = blob
  }
  getName() {
    return this.name
  }
  getURL() {
    return this.url
  }
  getBlob() {
    return this.blob
  }

}


export default LveJSAsset